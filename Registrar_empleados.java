    private void btnEliminarempleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarempleadosActionPerformed
        // TODO add your handling code here:
        int fila =tablaEmpleados.getSelectedRow();
        if (fila>=0){
            modelo.removeRow(fila);
            
        }
        else{
            JOptionPane.showMessageDialog(null,"Seleccionar Fila");
        }
    }//GEN-LAST:event_btnEliminarempleadosActionPerformed

    private void btnEditarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarEmpleadosActionPerformed
        // TODO add your handling code here:}
                
    int filaSeleccionada = tablaEmpleados.getSelectedRow();
    

    if (filaSeleccionada >= 0) {
       
        String nombre = modelo.getValueAt(filaSeleccionada, 0).toString();
        String apellido = modelo.getValueAt(filaSeleccionada, 1).toString();
        String cedula = modelo.getValueAt(filaSeleccionada, 2).toString();
        String numero = modelo.getValueAt(filaSeleccionada, 3).toString();
        String puesto = modelo.getValueAt(filaSeleccionada, 4).toString();
        String sueldo = modelo.getValueAt(filaSeleccionada, 5).toString();
        String ausencias = modelo.getValueAt(filaSeleccionada, 6).toString();
        
        
        txtNombre.setText(nombre);
        txtApellidos.setText(apellido);
        txtCedula.setText(cedula);
        txtNumero.setText(numero);
        txtPuesto.setText(puesto);
        txtSueldo.setText(sueldo);
        txtAusencias.setText(ausencias);
        
        
        
        modelo.removeRow(filaSeleccionada);
    } else {
        JOptionPane.showMessageDialog(null, "Seleccionar una fila para editar.");
    }
        
    }//GEN-LAST:event_btnEditarEmpleadosActionPerformed

